function getFesKumiwakeEmbed(thema, us) {
    let users = us;
    let fesKumiwakeEmbed = new MessageEmbed();
    fesKumiwakeEmbed.setColor('#00ff00');
    fesKumiwakeEmbed.setTitle(`${thema['A']} vs ${thema['B']}`);
    fesKumiwakeEmbed.setDescription('ランク35以下 or 最高ウデマエがC+以下の人は武器自由\n最高ウデマエがB- ~ S+の人はジャンルのみ指定\n最高ウデマエがXの人は武器指定');
    let usersSideA = [];
    let usersSideB = [];
    //各チーム4人ずつ抽選
    for (let i = 0; i < 4; i++) {
        let userNameA = '';
        let weaponNameA = '';
        let weaponClassA = '';
        let teamA = 'A';
        let userNameB = '';
        let weaponNameB = '';
        let weaponClassB = '';
        let teamB = 'B';

        //通常のABから優先的に抽選。人が居ない場合はredABから抽選。
        if (users['A'].length == 0) teamA = 'redA';
        if (users['B'].length == 0) teamB = 'redB';
        if (users['A'].length + users['redA'].length <= 0) break;
        if (users['B'].length + users['redB'].length <= 0) break;

        //チームAの人を抽選
        let temp = getRandomInt(users[teamA].length)
        userNameA = users[teamA][temp];
        users[teamA] = users[teamA].slice(0, temp).concat(users[teamA].slice(temp + 1));
        //チームBの人を抽選
        temp = getRandomInt(users[teamB].length)
        userNameB = users[teamB][temp];
        users[teamB] = users[teamB].slice(0, temp).concat(users[teamB].slice(temp + 1));

        //チームAの武器を抽選
        temp = getRandomInt(weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー')).length);
        weaponNameA = weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー'))[temp]['メイン'];
        weaponClassA = weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー'))[temp]['ジャンル'];

        //チームBの武器を抽選
        temp = getRandomInt(weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー')).length);
        weaponNameB = weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー'))[temp]['メイン'];
        weaponClassB = weaponsInfo.filter(e => !e['メイン'].includes('ヒーロー'))[temp]['ジャンル'];

        //メンバー名とそのジャンル（武器）を追加
        usersSideA.push(`${userNameA}：${weaponClassA}(${weaponNameA})`);
        usersSideB.push(`${userNameB}：${weaponClassB}(${weaponNameB})`);
    }


    if (usersSideA.join('\n') === '') usersSideA = ['なし'];
    if (usersSideB.join('\n') === '') usersSideB = ['なし'];
    fesKumiwakeEmbed.addField(name = thema['A'] + 'チーム', value = usersSideA.join('\n'), inline = false);
    fesKumiwakeEmbed.addField(name = thema['B'] + 'チーム', value = usersSideB.join('\n'), inline = false);

    // fesKumiwakeEmbed.setFooter('チームの勝利数に2勝以上差が付いた時は、勝ってる方のチームのウデマエ上位の人。');

    return { embeds: [fesKumiwakeEmbed] };

}

