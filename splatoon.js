//武器の名前を入れるとその名前を含む正式な名前の配列を返す。
function getWeaponByMain(name) {
    let wName = name;
    if (weapons.includes(name)) return weapons.filter(e => e.includes(name));
    else if (abbInfo.map(e => e.abb).filter(e => e === name)[0]) wName = abbInfo.filter(e => e.abb.includes(name)).map(e => e.main)[0];
    let regexp = new RegExp('.*' + wName.split('').join('.*') + '.*');
    return weapons.filter(e => e.match(regexp));

}

